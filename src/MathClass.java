public class MathClass {
    public static void main(String[] args) {
        double x = 2.1;
        double y = 5;
        // Whit Math Class we use a series of element to program
        // To access them you need to write "Math.element"
        System.out.println(Math.random()); // random number
        System.out.println(Math.ceil(x)); // round up
        System.out.println(Math.floor(y)); // round down
        System.out.println(Math.pow(x,y)); // x pow y
        System.out.println(Math.sqrt(y)); // square
    }
}
